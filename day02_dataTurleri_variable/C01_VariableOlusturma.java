package day02_dataTurleri_variable;

public class C01_VariableOlusturma {
    public static void main(String[] args) {
        int sayi;
        // Deklarasyon SADECE bir kez yapılabilir.
        // ama değer ataması istendiği kadar olabilir.

       // boolean sayi=20;

        // bir variable değer atamadan da deklare edilebilir
        // ancak değer atanmayan bir variable kullanilamaz.
       // System.out.println(sayi);
        sayi=20;
        System.out.println(sayi); // 21.satirdaki sayi variable^nin değeri 20
        sayi=sayi +10;
        System.out.println(sayi); // 25.satirda sayi variable'nin değeri 30

    }
}

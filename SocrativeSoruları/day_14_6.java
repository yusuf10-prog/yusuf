public class day_14_6 {
    public static void main(String[] args) {
        char ch='a';

        while (ch<'d'){
            System.out.println(ch + " ");
            ch++;
        }
        for (char i='a'; i<'d'; i++){
            System.out.println(i + " a şıkkı");
        }

    }
}
